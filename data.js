function normalizeTitleBaseForCsvMatch(title) {
  return String(title || "")
    .normalize("NFKC")
    .replace(/[！]/g, "!")
    .replace(/[？]/g, "?")
    .replace(/\s*[-–—]\s*full\s*size$/i, "")
    .replace(/\s+/g, " ")
    .trim();
}

function normalizeTitleForCsvMatch(title) {
  const base = normalizeTitleBaseForCsvMatch(title);
  const lowered = base.toLowerCase();
  return csvTitleAliasMap[lowered] || csvTitleAliasMap[base] || lowered;
}

function normalizeGroupForCsvMatch(group) {
  const value = String(group || "").normalize("NFKC").toLowerCase().trim();
  if (value.includes("love") || value === "=love") return "love";
  if (value.includes("≠me") || value.includes("not-equal-me") || value === "me") return "me";
  if (value.includes("≒joy") || value.includes("joy") || value === "joy") return "joy";
  if (value.includes("イコノイジョイ") || value === "all") return "all";
  return value;
}

// CSV照合の優先順位:
// 1) 完全一致 2) 個別マッピング一致 3) 正規化一致
function buildCsvMatchKeys(title, group) {
  const rawTitle = String(title || "").trim();
  const rawGroup = String(group || "").trim();
  const mappedTitle = normalizeTitleForCsvMatch(rawTitle);
  const normalizedTitle = normalizeTitleBaseForCsvMatch(rawTitle).toLowerCase();
  const normalizedGroup = normalizeGroupForCsvMatch(rawGroup);
  return {
    exact: `${rawTitle}::${rawGroup}`,
    mapped: `${mappedTitle}::${normalizedGroup}`,
    normalized: `${normalizedTitle}::${normalizedGroup}`,
  };
}

